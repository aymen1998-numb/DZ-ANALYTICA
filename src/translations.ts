export type Language = 'ar' | 'en' | 'fr';

// A simple translation hook generator to provide reactivity without context overhead
import { useState, useEffect } from 'react';

// Create a global state for language to avoid Prop Drilling or Context complexity for small apps
let currentLang: Language = 'ar';
if (typeof window !== 'undefined') {
  const urlParams = new URLSearchParams(window.location.search);
  const langParam = urlParams.get('lang');
  if (langParam === 'en' || langParam === 'fr' || langParam === 'ar') {
    currentLang = langParam;
  }
}
let listeners: Set<() => void> = new Set();

export const setLanguage = (lang: Language) => {
  currentLang = lang;
  listeners.forEach(l => l());
};

export const getLanguage = () => currentLang;

export const useTranslation = () => {
  const [lang, setLangState] = useState<Language>(currentLang);

  useEffect(() => {
    const handleLangChange = () => setLangState(currentLang);
    listeners.add(handleLangChange);
    return () => {
      listeners.delete(handleLangChange);
    };
  }, []);

  const t = (text: string) => {
    if (lang === 'ar') return text;
    // @ts-ignore
    return translations[lang]?.[text] || text;
  };

  const setLang = (newLang: Language) => {
    setLanguage(newLang);
  };

  return { t, lang, setLang };
};
